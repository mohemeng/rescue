#' finds the mean of an array
#'
#' @param x a numerical input
#'
#' @return the final value
#' @export
#'
#' @examples last(runif(10))
last <- function(x) {

  mean(x)
}


#' Finds the sum of two values
#'
#' @param a first value
#' @param b second value
#'
#' @return a sum of the first and second values
#' @export
#'
#' @examples jack(2, 3)
jack <- function(a, b){

  a + b
}
